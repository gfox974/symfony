    /**
     * Used to upgrade (rehash) the user's password automatically over time.
     */
    public function upgradePassword(UserInterface $author, string $newEncodedPassword): void
    {
        if (!$author instanceof Author) {
            throw new UnsupportedUserException(sprintf('Instances of "%s" are not supported.', \get_class($author)));
        }

        $author->setPassword($newEncodedPassword);
        $this->_em->persist($author);
        $this->_em->flush();
    }

